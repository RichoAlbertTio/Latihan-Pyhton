# const atau memberikan nilai tetap
Phi = 3.14

# mendeklarasikan variabel serta memberikan fungsi inputan dan tipe data yang di berikan
r = int(input("Masukan Jari-jari kerucut :"))
s = int(input("Masukan sisi-sisi kerucut :"))

# menyimpan rumus
Luas = Phi * r * (r * s)

# menampilkan hasil
print ("Luas permukaan kerucut :",Luas)