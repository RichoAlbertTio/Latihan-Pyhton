# mendeklarasikan variabel serta memberikan fungsi inputan dan tipe data yang di berikan
Bil1 = int(input("Masukan Bilangan Pertama :"))
Bil2 = int(input("Masukan Bilangan Kedua   :"))
# menyimpan rumus
tambah = Bil1 + Bil2
kurang = Bil1 - Bil2
kali = Bil1 * Bil2
bagi = Bil1 / Bil2
# menampilkan hasil
print ("Jadi hasil dari ", Bil1 , " + " , Bil2 , " = " , tambah)
print ("                ", Bil1 , " - " , Bil2 , " = " , kurang)
print ("                ", Bil1 , " * " , Bil2 , " = " , kali)
print ("                ", Bil1 , " / " , Bil2 , " = " , bagi)